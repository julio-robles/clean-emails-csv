-- Summary of the target coverage before touching production data
SELECT
  COUNT(*) AS total_targets,
  SUM(CASE WHEN s.`emailaddress` IS NOT NULL THEN 1 ELSE 0 END) AS found_in_database,
  SUM(CASE WHEN s.`listid` = 74 THEN 1 ELSE 0 END) AS currently_in_list_74,
  SUM(CASE WHEN s.`listid` = 55 THEN 1 ELSE 0 END) AS currently_in_list_55,
  SUM(CASE WHEN s.`listid` NOT IN (55, 74) THEN 1 ELSE 0 END) AS in_other_lists
FROM `tmp_move_contacts` t
LEFT JOIN `gr_wordpress`.`insp_list_subscribers` s
  ON s.`emailaddress` = t.`emailaddress`;

-- Ver cuntos correos estn en AMBAS listas (55 Y 74 simultneamente)
SELECT COUNT(*) AS emails_in_both_lists
FROM `tmp_move_contacts` t
WHERE EXISTS (
  SELECT 1 FROM `gr_wordpress`.`insp_list_subscribers` s55
  WHERE s55.`emailaddress` = t.`emailaddress` AND s55.`listid` = 55
)
AND EXISTS (
  SELECT 1 FROM `gr_wordpress`.`insp_list_subscribers` s74
  WHERE s74.`emailaddress` = t.`emailaddress` AND s74.`listid` = 74
);

-- Eliminar registros de la lista 55 si el correo ya existe en la lista 74
DELETE s55
FROM `gr_wordpress`.`insp_list_subscribers` s55
JOIN `tmp_move_contacts` t ON t.`emailaddress` = s55.`emailaddress`
JOIN `gr_wordpress`.`insp_list_subscribers` s74 
  ON s74.`emailaddress` = s55.`emailaddress` 
  AND s74.`listid` = 74
WHERE s55.`listid` = 55;

-- Cambiar los correos de la lista 55 a la lista 74 (solo los que NO estn ya en 74)
UPDATE `gr_wordpress`.`insp_list_subscribers` s
JOIN `tmp_move_contacts` t ON t.`emailaddress` = s.`emailaddress`
SET s.`listid` = 74
WHERE s.`listid` = 55;

-- Crear nuevos suscriptores para los correos que no existen en la base de datos
INSERT INTO `gr_wordpress`.`insp_list_subscribers` (
  `listid`,
  `emailaddress`,
  `domainname`,
  `format`,
  `confirmed`,
  `confirmcode`,
  `requestdate`,
  `requestip`,
  `confirmdate`,
  `confirmip`,
  `subscribedate`,
  `bounced`,
  `unsubscribed`,
  `unsubscribeconfirmed`,
  `formid`
)
SELECT
  74 AS `listid`,
  t.`emailaddress`,
  SUBSTRING_INDEX(t.`emailaddress`, '@', -1) AS `domainname`,
  'H' AS `format`,
  1 AS `confirmed`,
  REPLACE(UUID(), '-', '') AS `confirmcode`,
  UNIX_TIMESTAMP() AS `requestdate`,
  NULL AS `requestip`,
  UNIX_TIMESTAMP() AS `confirmdate`,
  NULL AS `confirmip`,
  UNIX_TIMESTAMP() AS `subscribedate`,
  0 AS `bounced`,
  0 AS `unsubscribed`,
  0 AS `unsubscribeconfirmed`,
  0 AS `formid`
FROM `tmp_move_contacts` t
WHERE NOT EXISTS (
  SELECT 1 
  FROM `gr_wordpress`.`insp_list_subscribers` s 
  WHERE s.`emailaddress` = t.`emailaddress`
);

-- Verificar resultados finales
SELECT 'AFTER_UPDATE' AS status, s.`listid`, COUNT(*) AS total
FROM `tmp_move_contacts` t
JOIN `gr_wordpress`.`insp_list_subscribers` s
  ON s.`emailaddress` = t.`emailaddress`
GROUP BY s.`listid`;

